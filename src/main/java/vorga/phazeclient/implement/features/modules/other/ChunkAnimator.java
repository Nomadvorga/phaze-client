    /**
     * Y offset (in blocks) the mixin should add to the chunk's model
     * offset uniform this frame. Returns 0 when the module is off,
     * the section's animation already finished, or this is the first
     * time we're seeing the section (in which case the start time is
     * recorded for next-frame interpolation; one frame at full drop
     * is imperceptible compared to the typical 600 ms duration).
     *
     * <p>Always returns a non-negative offset - chunks fall DOWN to
     * their target position, never overshoot below it.
     */
    public float getYOffset(BlockPos origin) {
        if (!isEnabled() || origin == null) {
            return 0.0F;
        }
        // Fade has no positional component (alpha-only via the
        // dither-discard fragment patch). Scale has no positional
        // component either (the section grows from its centre via
        // the vertex {@code mix(...)} patch, no world-space
        // translation). Vanilla (non-Sodium) users see no animation
        // under either mode because the vanilla path only honours
        // additive Y offsets; graceful degradation matches the
        // user-facing tooltip on the Animation Type dropdown.
        if (animationType.isSelected("Fade") || animationType.isSelected("Scale")) {
            return 0.0F;
        }

        // Per-column key (X,Z only) - all sections at the same chunk
        // X,Z share one first-seen timestamp so they animate in sync
        // instead of each Y stratum falling on its own clock. Without
        // this, sections of one chunk column become drawable at
        // slightly different times (Sodium uploads them as the build
        // tasks complete), and each starts its own independent
        // animation - producing the "fragmented chunk strips floating
        // at different heights" artifact reported by users under
        // sustained loading (elytra flight, low render-distance
        // pop-in, etc.).
        long key = ChunkPos.toLong(origin.getX() >> 4, origin.getZ() >> 4);
        long now = System.currentTimeMillis();
        long total = Math.max(1L, (long) duration.getInt());
        int dist = distance.getInt();

        Long start = firstSeenMs.get(key);
        if (start == null) {
            // Mirror writeRegionSectionYOffsets: only animate when
            // CHUNK_LOAD just told us about this column. Otherwise
            // (frustum-cull return, render-distance fade-in for an
            // already-loaded column, etc.) report 0 offset so the
            // chunk renders at its final position without a fresh
            // fall-in. Same exact gating as the per-section path so
            // toggling between vanilla and Sodium chunk renderers
            // gives consistent visual behaviour.
            if (!pendingAnimationKeys.contains(key)) {
                return 0.0F;
            }
            // Lazy eviction: amortise the cleanup cost against the
            // first-touch latency instead of running a timer thread.
            // Triggered only when the map gets fat enough to matter.
            if (firstSeenMs.size() >= MAX_TRACKED) {
                evictExpired(now, total);
            }
            pendingAnimationKeys.remove(key);
            firstSeenMs.put(key, now);
            lastAnimRegisterMs = now;
            return (float) dist;
        }

        long elapsed = now - start;
        if (elapsed >= total) {
            return 0.0F;
        }

        double progress = (double) elapsed / (double) total;
        Interpolation curve = Interpolations.getByName(easing.getSelected());
        double eased = curve.interpolate(progress);
        // (1 - eased) inverts the curve so we start at full drop and
        // approach 0 - the easing shapes the arrival, not the launch.
        double offset = dist * (1.0 - eased);
        // Clamp negative values from overshooting easings (Bounce,
        // Elastic) so the chunk never dips BELOW its final position
        // while landing - looks wrong with terrain.
        if (offset < 0.0) {
            offset = 0.0;
        }
        return (float) offset;
    }

    /** Drops sections whose animation finished comfortably in the past. */
    private void evictExpired(long now, long total) {
        evictExpiredFrom(firstSeenMs, now, total);
        // playerYAtRegister walks lock-step with firstSeenMs (both
        // keyed on the column key); drop any entry whose key is no
        // longer in firstSeenMs after the eviction above. Cheap O(N)
        // scan with O(1) containsKey lookups. pendingAnimationKeys is
        // populated/drained event-driven (CHUNK_LOAD fills, the first
        // matching frame drains) so it doesn't need eviction here -
        // it only ever holds entries waiting to be registered for the
        // first time and is bounded by the loaded-chunk count.
        playerYAtRegister.keySet().removeIf(k -> !firstSeenMs.containsKey(k));
    }

    /**
     * Per-section Y offsets for one Sodium {@link
     * net.caffeinemc.mods.sodium.client.render.chunk.region.RenderRegion}
     * packed into the layout the patched terrain vertex shader expects.
     *
     * <p>Sodium's chunk shader receives a per-vertex {@code _draw_id}
     * encoded as {@code (relX << 5) | (relZ << 2) | relY} with masks
     * {@code 7, 3, 7} (see Sodium's {@code chunk_vertex.glsl} -
     * {@code _get_relative_chunk_coord}). The output array uses the
     * same encoding so the shader can do {@code u_PhazeChunkAnimY[draw_id]}
     * and pick the right offset for the section that vertex belongs
     * to. {@code 8 * 4 * 8 = 256} entries cover every possible
     * section slot in a region; unused slots receive {@code 0.0F}
     * which is the no-op identity for the addition the shader does.
     *
     * <p>Each section's offset is computed exactly the same way as
     * the vanilla path: the per-section first-seen timestamp drives
     * the configured easing curve, and the offset is the distance
     * setting times {@code (1 - eased)}. So a freshly-seen section
     * inside an already-known region animates from the full drop
     * height the same way it would on the vanilla renderer - which
     * is what fixes the "elytra flight only animates whole regions"
     * bug under Sodium.
     *
     * <p>Allocates a fresh 1KB array each call. Even at render
     * distance 32 with ~30 visible regions per frame that's
     * {@code 30 * 1KB = 30KB} of throwaway allocation per frame,
     * which the GC absorbs without measurable impact.
     *
     * @param regionOriginBlockX region's block-coord X origin
     *                           (from {@code RenderRegion.getOriginX()})
     * @param regionOriginBlockY region's block-coord Y origin
     * @param regionOriginBlockZ region's block-coord Z origin
     * @return float[256] indexed by the shader's encoded draw_id,
     *         or all-zero array when the module is off
     */
    public boolean writeRegionSectionYOffsets(
            int regionOriginBlockX, int regionOriginBlockY, int regionOriginBlockZ,
            int[] drawableSlots, int drawableCount,
            float[] outOffsets) {
        // Always zero the output buffer so the caller can re-use it
        // across regions without seeing stale values from the previous
        // upload. java.util.Arrays.fill is intrinsified and faster
        // than a Java-level loop.
        java.util.Arrays.fill(outOffsets, 0, 256, 0.0F);
        if (!isEnabled() || drawableSlots == null || drawableCount <= 0) {
            return false;
        }
        // Fade routes through {@link #writeRegionSectionFadeValues}
        // and Scale routes through {@link #writeRegionSectionScaleValues}.
        // Returning false here leaves the GPU-side offset uniform at
        // the all-zero state the caller just wrote, which is exactly
        // the no-spatial-movement identity we want under either of
        // those modes - the actual animation is driven by their
        // dedicated progress arrays in the patched shaders.
        if (animationType.isSelected("Fade") || animationType.isSelected("Scale")) {
            return false;
        }

        // Region origin is in blocks; the section grid in Sodium is
        // 8 (X) * 4 (Y) * 8 (Z) sections. Each section is 16 blocks
        // on a side, so >> 4 turns the region origin into the
        // section/chunk coord of the region's first section.
        int regionChunkX = regionOriginBlockX >> 4;
        int regionChunkY = regionOriginBlockY >> 4;
        int regionChunkZ = regionOriginBlockZ >> 4;

        long now = System.currentTimeMillis();
        long total = Math.max(1L, (long) duration.getInt());
        int dist = distance.getInt();
        Interpolation curve = Interpolations.getByName(easing.getSelected());

        // Player Y at THIS frame - captured into playerYAtRegister
        // for any column that registers right now, then re-read from
        // that map for columns that already registered. The "from
        // player Y" semantic only kicks in for vertical animation
        // types (Top / Bottom); Side keeps the old behaviour
        // (constant magnitude in a horizontal direction) because
        // "distance blocks above the player" doesn't translate to
        // horizontal slide-in.
        MinecraftClient mc = MinecraftClient.getInstance();
        float currentPlayerY = (mc != null && mc.player != null)
                ? (float) mc.player.getY()
                : 0.0F;

        // verticalSign = +1 for Top (sections start ABOVE player Y),
        // -1 for Bottom (sections start BELOW player Y), 0 for Side
        // (we'll fall back to the constant-magnitude path below).
        float verticalSign;
        if (animationType.isSelected("Top")) {
            verticalSign = 1.0F;
        } else if (animationType.isSelected("Bottom")) {
            verticalSign = -1.0F;
        } else {
            verticalSign = 0.0F;
        }
        boolean verticalMode = verticalSign != 0.0F;

        // One eviction per region per frame at most - cheap, and
        // keeps the map from growing unbounded if the player is
        // teleporting around dropping section trackers everywhere.
        if (firstSeenMs.size() >= MAX_TRACKED) {
            evictExpired(now, total);
        }

        // Iterate ONLY the sections actually being drawn this frame
        // (Sodium's ChunkRenderList.sectionsWithGeometryIterator
        // produces these slot indices). Sections without geometry
        // - either not yet uploaded or unloaded - keep their slots
        // at the array's default 0.0F. This is what gives us the
        // per-section animation: as the player flies into a region,
        // sections finish uploading one by one, each registers a
        // first-seen timestamp at that moment, and animates from
        // full distance back to 0 over `duration` ms - matching the
        // vanilla path's per-section feel.
        boolean hasNonZero = false;
        for (int i = 0; i < drawableCount; i++) {
            int slot = drawableSlots[i] & 0xFF;
            // Decode (relX, relY, relZ) from the slot index using the
            // same packing the shader's _get_relative_chunk_coord
            // expects: pos >> (5, 0, 2) & (7, 3, 7). relY is needed
            // for the vertical "from-player-Y" mode below so each
            // section knows its own world Y; the per-column timestamp
            // key still ignores relY so all Y strata of one chunk
            // share an animation clock.
            int relX = (slot >> 5) & 7;
            int relY = slot & 3;
            int relZ = (slot >> 2) & 7;

            int sx = regionChunkX + relX;
            int sz = regionChunkZ + relZ;
            // Per-column (X,Z) key, NOT per-section (X,Y,Z). All
            // 24 sections of a single chunk column share one anim
            // timestamp so they slide in as a coherent vertical
            // strip. The previous per-section keying caused each
            // Y stratum of one chunk to start its own independent
            // animation - sections finish their build/upload at
            // slightly different times, so the "first seen" frame
            // differs per Y, producing the floating-fragments
            // artifact the user reported. ChunkPos.toLong packs
            // (x,z) into a long the same way ChunkSectionPos
            // packs (x,y,z), so the map keying remains O(1).
            long key = ChunkPos.toLong(sx, sz);

            // Section's bottom-Y in world space. Used by the vertical
            // "from-player-Y" mode to compute the per-section initial
            // offset (each section ends up at the same start WORLD Y,
            // which means the offset MAGNITUDE differs per section -
            // a high-Y section needs a smaller upward shift than a
            // low-Y one to land at "player Y + distance").
            int sectionWorldY = regionOriginBlockY + relY * 16;

            Long start = firstSeenMs.get(key);
            float offset;
            // Three states for a column's first frame in this iterator:
            //   1. start != null     -> already animating or finished;
            //                            fall through to the elapsed-time
            //                            branch below.
            //   2. start == null AND key in pendingAnimationKeys
            //                          -> Fabric's CHUNK_LOAD just told
            //                             us this chunk is fresh from the
            //                             server. Register and animate.
            //   3. start == null AND key NOT in pendingAnimationKeys
            //                          -> column was already loaded but is
            //                             only now becoming drawable
            //                             (typically a frustum-cull
            //                             return after the camera turned
            //                             around). Skip animation entirely
            //                             - no firstSeenMs entry, slot
            //                             stays at the buffer's default 0.
            // Case 3 is the whole point of the rewrite: turning around
            // used to re-trigger the animation because the only "is this
            // a re-appearance?" signal we had was a per-frame draw-gap
            // heuristic, which couldn't tell frustum culling apart from
            // genuine unload. CHUNK_LOAD is the precise per-chunk signal
            // for "fresh data", so we bypass the heuristic entirely.
            if (start == null && !pendingAnimationKeys.contains(key)) {
                continue;
            }
            if (start == null) {
                pendingAnimationKeys.remove(key);
                firstSeenMs.put(key, now);
                lastAnimRegisterMs = now;
                if (verticalMode) {
                    playerYAtRegister.put(key, currentPlayerY);
                    // Initial offset: section needs to start at
                    // (playerY + sign*dist) in world space, so its
                    // additive Y delta is (start - sectionWorldY).
                    // Sign of the result encodes direction:
                    //   Top: positive (section appears above)
                    //   Bottom: negative (section appears below)
                    // The shader applies this with u_PhazeChunkAnimDir
                    // forced to (0,1,0) - see writeAnimationDirection
                    // PerSection - so the sign rides cleanly through
                    // the multiplication.
                    float startWorldY = currentPlayerY + verticalSign * (float) dist;
                    offset = startWorldY - (float) sectionWorldY;
                } else {
                    // Side mode keeps the constant-magnitude semantics:
                    // every section shifts by `dist` along the chosen
                    // horizontal direction (the writeAnimationDirection
                    // vector). No per-section sectionWorldY math.
                    offset = (float) dist;
                }
            } else {
                long elapsed = now - start;
                if (elapsed >= total) {
                    // Common steady-state path: animation finished
                    // long ago, leave the buffer slot at 0 and skip
                    // the slot write entirely so the byte ends up in
                    // the GPU upload as a no-op.
                    continue;
                }
                double progress = (double) elapsed / (double) total;
                double eased = curve.interpolate(progress);
                if (verticalMode) {
                    Float storedPy = playerYAtRegister.get(key);
                    float py = storedPy != null ? storedPy : currentPlayerY;
                    float startWorldY = py + verticalSign * (float) dist;
                    float initial = startWorldY - (float) sectionWorldY;
                    offset = initial * (float) (1.0 - eased);
                    // Don't clamp at 0 - in vertical mode the sign of
                    // `offset` IS direction, so a negative value is
                    // valid (Bottom: section currently below final).
                    if (offset == 0.0F) {
                        continue;
                    }
                } else {
                    double off = dist * (1.0 - eased);
                    offset = off < 0.0 ? 0.0F : (float) off;
                    if (offset == 0.0F) {
                        // The easing landed exactly on 0 (Bounce can do
                        // this mid-curve). Skip the assign so we don't
                        // flag hasNonZero on what the GPU sees as a
                        // no-op anyway.
                        continue;
                    }
                }
            }

            outOffsets[slot] = offset;
            hasNonZero = true;
        }
        return hasNonZero;
    }

    /**
     * Convenience predicate. {@code true} when the user picked the
     * pure-alpha fade variant; consumers that branch on this choose
     * between uploading position offsets (Top/Bottom/Side) versus
     * per-section fade-progress values (Fade).
     */
    public boolean isFadeMode() {
        return animationType.isSelected("Fade");
    }

    /**
     * Convenience predicate. {@code true} when the user picked the
     * grow-from-centre variant; consumers that branch on this choose
     * between uploading position offsets (Top/Bottom/Side), per-section
     * fade-progress values (Fade), or per-section scale-progress
     * values (Scale).
     *
     * <p>Scale and Fade share the same 0..1 linear progress curve
     * (driven by {@link #duration} alone, no easing); only the
     * uniform they write to and the shader-side interpretation
     * differ. See {@link #writeRegionSectionScaleValues} for the
     * scale-progress write path.
     */
    public boolean isScaleMode() {
        return animationType.isSelected("Scale");
    }

    /**
     * Resolves the current animation type into the integer code the
     * patched vertex shader expects in its {@code u_PhazeChunkAnimMode}
     * uniform. This drives the three-branch dispatcher inside the
     * shader (offset add / fade varying / scale mix). The mapping
     * matches the patcher's documented contract:
     *
     * <ul>
     *   <li>{@code 0} - module disabled or no-op (identity vec4)</li>
     *   <li>{@code 1} - offset (Top, Bottom, Side)</li>
     *   <li>{@code 2} - fade (alpha-only via fragment shader)</li>
     *   <li>{@code 3} - scale (mix-to-centre via vertex shader)</li>
     * </ul>
     *
     * <p>Returning {@code 0} when disabled lets the mixin skip
     * uploading meaningful per-section data while still leaving the
     * UBO in a known-identity state - the shader's mode comparison
     * ensures every per-vertex branch becomes a no-op even if the
     * payload happens to carry stale values from before the user
     * disabled the module.
     */
    public int getAnimationModeIndex() {
        if (!isEnabled()) {
            return 0;
        }
        if (isFadeMode()) {
            return 2;
        }
        if (isScaleMode()) {
            return 3;
        }
        // Top / Bottom / Side - all routed through the offset-add
        // path in the patched vertex shader.
        return 1;
    }

    /**
     * Resolves {@link #fadeStyle} into the integer code the patched
     * fragment shader expects in its {@code u_PhazeFadeStyle}
     * uniform. The mapping is hard-coded to match the branches the
     * patcher injects:
     *
     * <ul>
     *   <li>{@code 0} - Dither (Bayer-discard, default)</li>
     *   <li>{@code 1} - Fog Mix (linear blend toward {@code u_FogColor})</li>
     * </ul>
     *
     * <p>Any unrecognised selection falls back to {@code 0} so a
     * corrupted config can't drive the shader into an undefined
     * branch (uninitialised int reads as 0 in GLSL too, so {@code 0}
     * is the natural "do nothing weird" default).
     */
    public int getFadeStyleIndex() {
        return 1;
    }

    /**
     * Per-section fade-progress for one Sodium region. Companion to
     * {@link #writeRegionSectionYOffsets} - mutually exclusive with
     * it, since Fade is its own animation type and the position
     * offsets are forced to zero in that case.
     *
     * <p>Each slot gets a {@code [0..1]} progress value where
     * {@code 0} means "fully transparent / about to dissolve in" and
     * {@code 1} means "fully visible / no discard". The shader-side
     * patch compares this varying against a 4x4 Bayer threshold
     * keyed on {@code gl_FragCoord.xy} and {@code discard}s the
     * fragment when the threshold beats the progress, producing the
     * dithered fade-in effect.
     *
     * <p>Progress is intentionally <em>linear</em> ({@code elapsed /
     * total}) - the visual interest comes from the dither pattern,
     * not from an easing curve, and the spec the user requested is
     * "Fade, no interpolations, just duration". Slots that aren't
     * mid-animation (frustum returns, already finished, never
     * registered) get {@code 1.0} so the shader leaves them alone.
     *
     * <p>Returns {@code true} when at least one slot is below 1.0
     * (i.e., still mid-fade), so the caller can skip the {@code
     * glUniform1fv} upload when the entire region has already
     * settled - mirroring the {@code hasNonZero} convention from
     * {@link #writeRegionSectionYOffsets}.
     */
    public boolean writeRegionSectionFadeValues(
            int regionOriginBlockX, int regionOriginBlockY, int regionOriginBlockZ,
            int[] drawableSlots, int drawableCount,
            float[] outFade) {
        // Default every slot to "fully opaque" so the shader's
        // {@code v_PhazeChunkAnimFade < 1.0} branch is skipped for
        // anything we don't explicitly mark as animating. Critical
        // for the steady-state case: a region that's just sitting
        // there uploads all-1.0s and the fragment-shader cost stays
        // at one comparison per pixel.
        java.util.Arrays.fill(outFade, 0, 256, 1.0F);
        return writeRegionSectionProgressInternal(
                regionOriginBlockX, regionOriginBlockZ,
                drawableSlots, drawableCount,
                outFade,
                isFadeMode());
    }

    /**
     * Per-section scale-progress for one Sodium region. Companion to
     * {@link #writeRegionSectionFadeValues} - same 0..1 linear ramp
     * driven by {@link #duration}, same per-column timestamp keying,
     * differs only in where the patched shader plugs the value in:
     *
     * <ul>
     *   <li>Fade: the value lands in {@code u_PhazeChunkAnimFade}
     *       and the fragment shader dithers / blends it.</li>
     *   <li>Scale: the value lands in {@code u_PhazeChunkAnimScale}
     *       and the vertex shader {@code mix()}es the chunk-local
     *       position toward the section centre when below 1.0,
     *       collapsing the section to a point and growing it back
     *       out as the value ramps to 1.0.</li>
     * </ul>
     *
     * <p>Returns {@code true} when at least one slot is below 1.0
     * (i.e., still mid-grow), so the caller can short-circuit the
     * GPU upload once the entire region has settled. Mirrors the
     * {@code hasNonOne} convention used by
     * {@link #writeRegionSectionFadeValues}.
     *
     * <p>Defaults to 1.0 (no shrink) for every slot before the
     * progress loop runs - so disabled / non-Scale modes and slots
     * the per-region iterator skips never collapse anything to a
     * point. This is the same defensive default as the fade-values
     * write path.
     */
    public boolean writeRegionSectionScaleValues(
            int regionOriginBlockX, int regionOriginBlockY, int regionOriginBlockZ,
            int[] drawableSlots, int drawableCount,
            float[] outScale) {
        java.util.Arrays.fill(outScale, 0, 256, 1.0F);
        return writeRegionSectionProgressInternal(
                regionOriginBlockX, regionOriginBlockZ,
                drawableSlots, drawableCount,
                outScale,
                isScaleMode());
    }

    /**
     * Shared per-section progress-write loop for Fade and Scale.
     * Caller pre-fills {@code out} with the steady-state value
     * (1.0F) and passes {@code active=true} only when the relevant
     * animation mode is selected; this method then overwrites slots
     * for sections that are currently mid-animation with their 0..1
     * linear progress value.
     *
     * <p>Three-case state machine per slot, identical to the offset
     * path's gating logic:
     *
     * <ol>
     *   <li>Column has a {@code firstSeenMs} timestamp - it's
     *       animating or finished; compute elapsed / total and
     *       write the slot.</li>
     *   <li>Column has no timestamp but is in
     *       {@link #pendingAnimationKeys} (fresh
     *       {@code CHUNK_LOAD}) - register the timestamp and emit
     *       {@code 0.0F} this frame (full transparency / full
     *       shrink).</li>
     *   <li>Column has no timestamp and isn't pending (frustum
     *       return) - leave the slot at the default {@code 1.0F}
     *       so no animation plays.</li>
     * </ol>
     *
     * <p>{@code regionOriginBlockY} is intentionally absent from the
     * parameter list: column keys ignore Y so every section in a
     * vertical strip shares one timestamp and animates in sync (see
     * {@link #writeRegionSectionFadeValues}'s docstring for the
     * fragmented-strip artifact this prevents).
     */
    private boolean writeRegionSectionProgressInternal(
            int regionOriginBlockX, int regionOriginBlockZ,
            int[] drawableSlots, int drawableCount,
            float[] out,
            boolean active) {
        if (!isEnabled() || !active || drawableSlots == null || drawableCount <= 0) {
            return false;
        }

        int regionChunkX = regionOriginBlockX >> 4;
        int regionChunkZ = regionOriginBlockZ >> 4;

        long now = System.currentTimeMillis();
        long total = Math.max(1L, (long) duration.getInt());

        if (firstSeenMs.size() >= MAX_TRACKED) {
            evictExpired(now, total);
        }

        boolean hasNonOne = false;
        for (int i = 0; i < drawableCount; i++) {
            int slot = drawableSlots[i] & 0xFF;
            // Same slot decoding as the offset path - section
            // identity is keyed per-column (X,Z) so every Y stratum
            // of one chunk shares one progress clock. Without this
            // each Y stratum starts its own animation at slightly
            // different times (Sodium uploads them as build tasks
            // complete), producing a horizontally-striped effect
            // instead of a coherent one.
            int relX = (slot >> 5) & 7;
            int relZ = (slot >> 2) & 7;
            int sx = regionChunkX + relX;
            int sz = regionChunkZ + relZ;
            long key = ChunkPos.toLong(sx, sz);

            Long start = firstSeenMs.get(key);
            if (start == null && !pendingAnimationKeys.contains(key)) {
                continue;
            }
            if (start == null) {
                pendingAnimationKeys.remove(key);
                firstSeenMs.put(key, now);
                lastAnimRegisterMs = now;
                // First frame: fully invisible / fully collapsed.
                // The dither pattern (Fade) or position mix (Scale)
                // takes it from this value up to 1.0 over duration.
                out[slot] = 0.0F;
                hasNonOne = true;
                continue;
            }
            long elapsed = now - start;
            if (elapsed >= total) {
                // Animation finished - leave at 1.0 (the default
                // already written by Arrays.fill in the caller).
                continue;
            }
            // Linear progress. Both modes are specced as "just
            // duration, no interpolations" - Fade because the dither
            // pattern does the visual lifting, Scale because the
            // grow-from-centre effect reads as natural at uniform
            // speed and easing would just stretch the "ugly mid-
            // grow" frame.
            float progress = (float) elapsed / (float) total;
            out[slot] = progress;
            hasNonOne = true;
        }
        return hasNonOne;
    }

    /**
     * Region-level magnitude for the Sodium fallback path. Used when
     * the per-section shader patch couldn't apply - typically because
     * Iris is active with a shader pack and the chunk programs are
     * compiled from the shader pack source instead of Sodium's
     * default {@code block_layer_opaque.vsh}, so our injected
     * {@code u_PhazeChunkAnimOffset} / {@code u_PhazeChunkAnimDir}
     * uniforms aren't present.
     *
     * <p>Returns the region's current offset magnitude based on
     * {@link #regionFirstSeenMs} - the region animates from
     * {@code distance} down to {@code 0} over {@code duration} ms
     * once, starting the first time this method is called for the
     * region. New sections appearing within the same region while
     * its animation is already in progress (or already finished) do
     * NOT extend or restart the animation; they appear at whatever
     * the region's current offset is. This is the only way to avoid
     * the region "snapping back to full distance" each time a new
     * section first becomes drawable inside it, which is the bulk
     * of what happens during sustained elytra flight.
     *
     * <p>Side-effect: registers first-seen time in
     * {@link #regionFirstSeenMs} for the region (not the section).
     * The {@code drawableSlots} / {@code drawableCount} parameters
     * are still accepted for API symmetry with
     * {@link #writeRegionSectionYOffsets} but are only used as the
     * "is this region drawable at all this frame?" signal - if
     * {@code drawableCount <= 0} we return zero magnitude without
     * registering anything.
     */
    public float getRegionMagnitude(int regionOriginBlockX, int regionOriginBlockY, int regionOriginBlockZ,
                                    int[] drawableSlots, int drawableCount) {
        if (!isEnabled() || drawableSlots == null || drawableCount <= 0) {
            return 0.0F;
        }

        // Region-level animation: each region animates exactly once,
        // when its first set of drawable sections becomes visible.
        // Subsequent new sections appearing within the same region
        // (which is the common case during sustained elytra flight)
        // do NOT re-trigger the animation - they just appear at the
        // region's current offset (settling toward 0). Without this
        // single-shot per-region behaviour, every new section in an
        // already-seen region would force the whole 8x4x8 block back
        // to full distance, producing the user-visible "chunks snap
        // and fall ~10 times in a row" symptom under shaders.
        int regionChunkX = regionOriginBlockX >> 4;
        int regionChunkY = regionOriginBlockY >> 4;
        int regionChunkZ = regionOriginBlockZ >> 4;
        long regionKey = ChunkSectionPos.asLong(regionChunkX, regionChunkY, regionChunkZ);

        long now = System.currentTimeMillis();
        long total = Math.max(1L, (long) duration.getInt());
        int dist = distance.getInt();

        if (regionFirstSeenMs.size() >= MAX_TRACKED) {
            evictExpiredFrom(regionFirstSeenMs, now, total);
        }

        Long start = regionFirstSeenMs.get(regionKey);
        if (start == null) {
            regionFirstSeenMs.put(regionKey, now);
            lastAnimRegisterMs = now;
            return (float) dist;
        }

        long elapsed = now - start;
        if (elapsed >= total) {
            return 0.0F;
        }

        Interpolation curve = Interpolations.getByName(easing.getSelected());
        double progress = (double) elapsed / (double) total;
        double eased = curve.interpolate(progress);
        return (float) (dist * (1.0 - eased));
    }

    /** Shared eviction primitive for both per-section and per-region trackers. */
    private static void evictExpiredFrom(Map<Long, Long> map, long now, long total) {
        long cutoff = now - 2L * total;
        Iterator<Map.Entry<Long, Long>> it = map.entrySet().iterator();
        while (it.hasNext()) {
            if (it.next().getValue() < cutoff) {
                it.remove();
            }
        }
    }

    /**
     * Reset all per-section animation timestamps. Wired from the
     * {@code onGameJoin} / {@code onPlayerRespawn} S2C packet
     * handlers, which fire exactly once per real {@code ClientWorld}
     * swap. Without this hook the tracker would still hold
     * "already animated" timestamps from the previous world and
     * newly-loaded chunks at colliding section coords would skip
     * the animation.
     *
     * <p>Safe to call when the module is disabled - the map is just
     * idle in that state, and clearing it has no side effect on
     * vanilla.
     */
    public void resetTracker() {
        // Called from ClientPlayNetworkHandlerChunkAnimatorResetMixin
        // on onGameJoin / onPlayerRespawn only - i.e. exactly once
        // per actual ClientWorld swap. The old reload()-based hook
        // was removed because Iris fires reload() in bursts on every
        // pipeline rebuild (shader pack apply, settings change, etc.),
        // which would clear an in-flight animation mid-fall.
        firstSeenMs.clear();
        regionFirstSeenMs.clear();
        playerYAtRegister.clear();
        // Drop pending CHUNK_LOAD requests too - the events that
        // populated them fired against the previous world, and the
        // new world will fire its own CHUNK_LOAD batch as Minecraft
        // walks the freshly-received chunk packets.
        pendingAnimationKeys.clear();
    }

    /**
     * F3+A handler: re-trigger the fall-in animation for every
     * currently-loaded chunk. Walks the player's render-distance
     * square, drops every column's stale {@link #firstSeenMs} /
     * {@link #playerYAtRegister} entry (otherwise the "elapsed &gt;=
     * total" steady-state branch would short-circuit the next draw
     * and skip the animation), and seeds {@link
     * #pendingAnimationKeys} so the per-section path treats each
     * column as freshly loaded on its next visible frame - exactly
     * the gate {@link #writeRegionSectionYOffsets} uses to decide
     * "animate" versus "frustum-cull return, skip".
     *
     * <p>Triggered from {@code KeyboardChunkAnimatorResetMixin}
     * which intercepts the specific {@code WorldRenderer#reload()}
     * INVOKE inside {@code Keyboard#processF3} - that call site is
     * reachable only from the {@code GLFW_KEY_A} branch, so Iris's
     * pipeline-rebuild reloads (which call {@code reload()} from
     * their own manager) don't reach it. Safe to invoke when the
     * module is disabled: the per-frame path still gates on
     * {@link #isEnabled()}, so seeding pending keys here is a no-op
     * visually until the user re-enables Chunk Animator.
     */
    public void onF3AReload() {
        firstSeenMs.clear();
        regionFirstSeenMs.clear();
        playerYAtRegister.clear();
        // No CHUNK_LOAD will fire on F3+A (the WorldChunk objects
        // aren't reloaded - only Sodium's GPU-side render data is
        // rebuilt), so we have to populate pendingAnimationKeys by
        // walking the loaded-chunk square ourselves. Without this
        // the per-section path would see start==null and the key
        // missing from pendingAnimationKeys, treat the section as
        // "frustum-cull return", and skip the animation entirely.
        MinecraftClient mc = MinecraftClient.getInstance();
        if (mc == null || mc.world == null || mc.player == null) {
            return;
        }
        int playerChunkX = mc.player.getChunkPos().x;
        int playerChunkZ = mc.player.getChunkPos().z;
        // Pull render distance from the options, with a +1 safety
        // margin: ClientChunkManager keeps a chunk cache sized at
        // (viewDistance + 3) per side, so anything visible right now
        // is within (viewDistance + 1) chunks of the player. The
        // tighter bound here avoids marking chunks the player can't
        // actually see yet, while still covering the entire view
        // frustum in every direction.
        int viewDistance = mc.options.getViewDistance().getValue() + 1;
        for (int dx = -viewDistance; dx <= viewDistance; dx++) {
            for (int dz = -viewDistance; dz <= viewDistance; dz++) {
                int cx = playerChunkX + dx;
                int cz = playerChunkZ + dz;
                // getChunk(x, z, FULL, false) returns null when the
                // chunk hasn't fully loaded; we only animate fully-
                // loaded chunks anyway since partial ones don't have
                // sections built yet.
                if (mc.world.getChunk(cx, cz, net.minecraft.world.chunk.ChunkStatus.FULL, false) != null) {
                    pendingAnimationKeys.add(ChunkPos.toLong(cx, cz));
                }
            }
        }
    }
